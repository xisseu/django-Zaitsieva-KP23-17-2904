#!/bin/bash

# Скрипт развертывания Django-приложения Task Tracker на Replit
# Автор шаблона: Зайцева П.В.
# Назначение: подготовка проекта, проверка миграций и запуск сервера Django

echo "Начало развертывания Django-приложения Task Tracker"

# Проверка наличия основного файла управления Django-проектом
if [ ! -f "manage.py" ]; then
  echo "Ошибка: файл manage.py не найден. Скрипт должен запускаться из корня проекта."
  exit 1
fi

# Проверка установленного Django
python -m django --version

# Выполнение миграций базы данных
python manage.py migrate

# Проверка структуры проекта без запуска сервера
python manage.py check

echo "Развертывание завершено успешно."
echo "Для запуска приложения используйте команду:"
echo "python manage.py runserver 0.0.0.0:8000"